package operation.pojo.topics;

import java.util.List;

public class TopicNearResponse {
	private List<TopicResponse> nearTopic;
	private long topicCount;
	public TopicNearResponse(List<TopicResponse> nearTopic,long topicCount){
		this.nearTopic = nearTopic;
		this.topicCount = topicCount;
	}
	
	public List<TopicResponse> getNearTopic() {
		return nearTopic;
	}
	public void setNearTopic(List<TopicResponse> nearTopic) {
		this.nearTopic = nearTopic;
	}
	public long getTopicCount() {
		return topicCount;
	}
	public void setTopicCount(long topicCount) {
		this.topicCount = topicCount;
	}

}
