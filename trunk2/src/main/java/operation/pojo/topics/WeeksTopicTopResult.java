package operation.pojo.topics;

import org.springframework.data.annotation.Id;

public class WeeksTopicTopResult {
	@Id
	private String id;
	
	private String topicTopResultId;
	
	private long ctime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTopicTopResultId() {
		return topicTopResultId;
	}

	public void setTopicTopResultId(String topicTopResultId) {
		this.topicTopResultId = topicTopResultId;
	}

	public long getCtime() {
		return ctime;
	}

	public void setCtime(long ctime) {
		this.ctime = ctime;
	}

}
