package operation.pojo.user;

import java.util.List;

import operation.pojo.file.FileOperationInfo;

import org.springframework.data.annotation.Id;

public class MyCollect {
	@Id
	private String Id;

	private String name;
	
	private List<FileOperationInfo> catagory;
	
	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<FileOperationInfo> getCatagory() {
		return catagory;
	}

	public void setCatagory(List<FileOperationInfo> catagory) {
		this.catagory = catagory;
	}




}
