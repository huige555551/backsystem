package operation.pojo.course;

import java.util.List;

import operation.pojo.user.ResponseUser;

public class ResponseUserGroupCourse {
	private String id;
	private String userId;
	private String groupId;
	private Object course;
	private List<UserChapter> userChapters;
	private int chapterNum; //此课程下章节数量
	private int chapterStudedNum; //此课程下用户已经学完的章节数量
	private boolean studyed;//此课程是否已经学习完毕
	private boolean faved;//此课程是否被收藏
	private long ctime;  //创建时间
	private long utime;//更新时间
	private List<ResponseUser>  responseUser;//用户收藏，学习列表
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public Object getCourse() {
		return course;
	}
	public void setCourse(Object course) {
		this.course = course;
	}
	public List<UserChapter> getUserChapters() {
		return userChapters;
	}
	public void setUserChapters(List<UserChapter> userChapters) {
		this.userChapters = userChapters;
	}
	public int getChapterNum() {
		return chapterNum;
	}
	public void setChapterNum(int chapterNum) {
		this.chapterNum = chapterNum;
	}
	public int getChapterStudedNum() {
		return chapterStudedNum;
	}
	public void setChapterStudedNum(int chapterStudedNum) {
		this.chapterStudedNum = chapterStudedNum;
	}
	public boolean isStudyed() {
		return studyed;
	}
	public void setStudyed(boolean studyed) {
		this.studyed = studyed;
	}
	public boolean isFaved() {
		return faved;
	}
	public void setFaved(boolean faved) {
		this.faved = faved;
	}
	public long getCtime() {
		return ctime;
	}
	public void setCtime(long ctime) {
		this.ctime = ctime;
	}
	public long getUtime() {
		return utime;
	}
	public void setUtime(long utime) {
		this.utime = utime;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public List<ResponseUser> getResponseUser() {
		return responseUser;
	}
	public void setResponseUser(List<ResponseUser> responseUser) {
		this.responseUser = responseUser;
	}
}
