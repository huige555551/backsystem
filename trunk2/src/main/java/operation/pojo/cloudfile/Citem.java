package operation.pojo.cloudfile;

/** 
 * @ClassName: Fitem
 * @Description: 前段使用，只存不做操作
 * @author yaoj
 * @date 2014年12月17日 上午9:17:22
 * 
 */
public class Citem {
	
	private String format;//转码后的格式：pdf, html4,html5,mp4,flv

	
	private String cmd;//云处理操作列表，包含每个云处理操作的状态信息。


	private int code;//int状态码，0（成功），1（等待处理），2（正在处理），3（处理失败），4（通知提交失败）。

	
	private String error;//如果处理失败，该字段会给出失败的详细原因。


	private String hash;//云处理结果保存在服务端的唯一hash标识。

	
	private String key;//云处理结果的外链资源名
	
	private String furl;	//全路径

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getCmd() {
		return cmd;
	}

	public void setCmd(String cmd) {
		this.cmd = cmd;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}

	public String getHash() {
		return hash;
	}

	public void setHash(String hash) {
		this.hash = hash;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getFurl() {
		return furl;
	}

	public void setFurl(String furl) {
		this.furl = furl;
	}
    
	
	
	
}
