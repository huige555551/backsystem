package operation.pojo.live;

public class VhallWebinar {

    /// 活动主题
    public String subject;

    /// 活动开始时间
    public String t_start;

    /// 活动结束时间
    public String t_end;

    /// 主持人姓名
    public String host;

    /// 是否开启第三方K值验证
    public int exist_3rd_auth;

    /// 第三方K值验证接口URL
    public String auth_url;

    /// 第三方K值验证失败跳转URL
    public String auth_failure_url;

    /// 活动描述
    public String webinar_desc;

    /// 公共密码
    public String channel_pass;
    
    

	public VhallWebinar() {
		super();
	}

	public VhallWebinar(String subject, String t_start, String t_end,
			String host, int exist_3rd_auth, String auth_url,
			String auth_failure_url, String webinar_desc, String channel_pass) {
		super();
		this.subject = subject;
		this.t_start =t_start;
		this.t_end = t_end;
		this.host = host;
		this.exist_3rd_auth = exist_3rd_auth;
		this.auth_url = auth_url;
		this.auth_failure_url = auth_failure_url;
		this.webinar_desc = webinar_desc;
		this.channel_pass = channel_pass;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getT_start() {
		return t_start;
	}

	public void setT_start(String t_start) {
		this.t_start = t_start;
	}

	public String getT_end() {
		return t_end;
	}

	public void setT_end(String t_end) {
		this.t_end = t_end;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getExist_3rd_auth() {
		return exist_3rd_auth;
	}

	public void setExist_3rd_auth(int exist_3rd_auth) {
		this.exist_3rd_auth = exist_3rd_auth;
	}

	public String getAuth_url() {
		return auth_url;
	}

	public void setAuth_url(String auth_url) {
		this.auth_url = auth_url;
	}

	public String getAuth_failure_url() {
		return auth_failure_url;
	}

	public void setAuth_failure_url(String auth_failure_url) {
		this.auth_failure_url = auth_failure_url;
	}

	public String getWebinar_desc() {
		return webinar_desc;
	}

	public void setWebinar_desc(String webinar_desc) {
		this.webinar_desc = webinar_desc;
	}

	public String getChannel_pass() {
		return channel_pass;
	}

	public void setChannel_pass(String channel_pass) {
		this.channel_pass = channel_pass;
	}
	
}
