package operation.service.queue;

import org.springframework.data.annotation.Id;

public class WorkNumber {
	@Id
	private String id;
	
	private String workName;
	
	private String userIdOrGroupId;
	
	public WorkNumber(){
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getWorkName() {
		return workName;
	}

	public void setWorkName(String workName) {
		this.workName = workName;
	}

	public String getUserIdOrGroupId() {
		return userIdOrGroupId;
	}

	public void setUserIdOrGroupId(String userIdOrGroupId) {
		this.userIdOrGroupId = userIdOrGroupId;
	}



}
